package Entity;

public class BaseEntity {
	private String ID;
	public String getID() {
		return ID;
	}
	public void setID(String id) {
		this.ID = id;
	}	
	
	private String FullName;
	public String getFullName() {
		return FullName;
	}
	public void setFullName(String fullName) {
		this.FullName = fullName;
	}
}
